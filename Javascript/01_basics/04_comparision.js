console.log(2 > 1);
console.log(2 >= 1);
console.log(2 < 1);
console.log(2 == 1);
console.log(2 != 1);

// JavaScript automatically converts the string to a number when using <, >, >=, <=
console.log("2" > 1);
console.log("02" > 1);

// java script will convert null to 0
console.log(null > 0);
console.log(null == 0);
console.log(null >= 0);

// undefined is not converted to a number in a meaningful way
console.log(undefined == 0);
console.log(undefined > 0);
console.log(undefined < 0);

// === 

console.log("2" === 2);